package projects;

public class Studentdetails {
 private String name;
 private int id;
 private String classStudying;
 private String examSub;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getClassStudying() {
	return classStudying;
}
public void setClassStudying(String classStudying) {
	this.classStudying = classStudying;
}
public String getExamSub() {
	return examSub;
}
public void setExamSub(String examSub) {
	this.examSub = examSub;
}
 
}
